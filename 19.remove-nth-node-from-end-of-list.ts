/**
 * Definition for singly-linked list.
 * class ListNode {
 *     val: number
 *     next: ListNode | null
 *     constructor(val?: number, next?: ListNode | null) {
 *         this.val = (val===undefined ? 0 : val)
 *         this.next = (next===undefined ? null : next)
 *     }
 * }
 */

function removeNthFromEnd(head: ListNode | null, n: number): ListNode | null {
    if (!head || n === 1 && !head.next) return null
    let l = head; // going to end up n + 1 behind head
    let r = head; // to find the end of the list
    let i = 0;
    while (i < n) {
        r = r.next
        i++
    }
    if (!r) return head.next

    while (r.next) {
        r = r.next;
        l = l.next
    }

    l.next = l.next.next
    return head


};
